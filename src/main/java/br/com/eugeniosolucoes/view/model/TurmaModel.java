package br.com.eugeniosolucoes.view.model;

/**
 *
 * @author eugenio
 */
public class TurmaModel {

    private String id;

    private String nome;

    public TurmaModel() {
    }

    public TurmaModel(String id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return nome;
    }

}
